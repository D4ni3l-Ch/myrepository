package ec.edu.espe.fitnessgym.model;

/**
 *
 * @author Daniel
 */
public class Customer {
    private int id;
    private String name;
    private int age;
    private float weight;
    private float height;

    public Customer() {
    }
    
    
    public Customer(int id, String name, int age, float weight, float heigth) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.height = heigth;
    }
    
    
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the weight
     */
    public float getWeight() {
        return weight;
    }

    /**
     * @param weight the weight to set
     */
    public void setWeight(float weight) {
        this.weight = weight;
    }

    /**
     * @return the height
     */
    public float getHeight() {
        return height;
    }

    /**
     * @param height the height to set
     */
    public void setHeight(float height) {
        this.height = height;
    }
    
}
