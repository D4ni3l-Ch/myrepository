    private void btnSaveCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveCustomerActionPerformed
        
        Customer customer = new Customer();
        
        customer.setId(Integer.parseInt(txtId.getText()));
        customer.setName(txtName.getText());
        customer.setAge(Integer.parseInt(txtAge.getText()));
        customer.setWeight(Float.parseFloat(txtWeight.getText()));
        customer.setHeight(Float.parseFloat(txtHeight.getText()));
        
        CustomerController controller = new CustomerController();
        
        controller.createCustomer(customer);
        
        loadCustomersTable();
        
    }//GEN-LAST:event_btnSaveCustomerActionPerformed

    private void btnCalculateTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateTotalActionPerformed
        CustomerController controller = new CustomerController();
        
        List<Customer> customers = controller.findAllCustomers();
              
        float total = controller.calculateTotalWeightHeight(customers);
        
        JOptionPane.showMessageDialog(null, total);
        
    }//GEN-LAST:event_btnCalculateTotalActionPerformed
    private void loadCustomersTable() {

        CustomerController controller = new CustomerController();
        
        List<Customer> customers = controller.findAllCustomers();
        List<Float> bmiList = controller.calculateBMIForCustomers(customers);
        
        DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
        
        model.setRowCount(0); 

        for (int i = 0; i < customers.size(); i++) {
            
            Customer c = customers.get(i);
            float bmi = bmiList.get(i);
            Object[] row = {c.getId(),c.getName(),c.getAge(),c.getWeight(),c.getHeight(),bmi};
                
            model.addRow(row);
        
        }
    }

